    private void btRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRegisterActionPerformed
        StaffMember objMember = new StaffMember();
        objMember.setPosition(cbPosition.getSelectedItem().toString());
        objMember.setFirstName(txtFirstName.getText());
        objMember.setLastName(txtLastName.getText());
        if (rdoFemale.isSelected()) {
            objMember.setGender("Female");
        } else {
            objMember.setGender("Male");
        }
        objMember.setMemberID(txtMemberID.getText());
        objMember.setUsername(txtUsername.getText());
        objMember.setPassword(txtPassword.getText());

        SaveToDatabase(objMember);
        new MainScreen().setVisible(true);
        this.setVisible(false);

    }//GEN-LAST:event_btRegisterActionPerformed

