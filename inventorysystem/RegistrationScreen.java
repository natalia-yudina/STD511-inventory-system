    private void SaveToDatabase(StaffMember newMember) {

        try {

            Connection con = InventorySystem.getConnectionInventorySystem();
            PreparedStatement pst = con.prepareStatement("INSERT INTO staff_members (first_name, last_name, gender, position, username, password, member_ID) VALUES (?, ?, ?, ?, ?, ?, ?);");
            pst.setString(1, newMember.getFirstName());
            pst.setString(2, newMember.getLastName());
            pst.setString(3, newMember.getGender());
            pst.setString(4, newMember.getPosition());
            pst.setString(5, newMember.getUsername());
            pst.setString(6, newMember.getPassword());
            pst.setString(7, newMember.getMemberID());
            int result = pst.executeUpdate();
            if (result == 1) {
                System.out.println("Registered Succesfully.");
                //JOptionPane.showMessageDialog(null, "Registered Succesfully.");
            }
        } catch (HeadlessException | SQLException e) {
            //JOptionPane.showMessageDialog(null,e.toString());
        }

    }

